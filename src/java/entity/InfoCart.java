package entity;

/**
 *
 * @author Derek
 */
public class InfoCart {
        private int uid,pid,price;
    private String name,image;

    public InfoCart(int uid, int pid, int price, String name, String image) {
        this.uid = uid;
        this.pid = pid;
        this.price = price;
        this.name = name;
        this.image = image;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
}
